export function assertAdminDomContract(elements) {
  const missing = REQUIRED_ELEMENT_KEYS.filter((key) => !elements[key]);
  if (!missing.length) return;

  throw new Error(`Admin DOM contract mismatch. Missing element ids: ${missing.join(", ")}`);
}
